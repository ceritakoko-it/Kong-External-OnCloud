import os
import re
import sys

root = sys.argv[1]

def clean(value: str) -> str:
    return value.strip().strip('"').strip("'")

partial_defs = set()
partial_def_files = []
partial_refs = []

for dirpath, _, filenames in os.walk(root):
    for filename in filenames:
        if not filename.endswith((".yaml", ".yml")):
            continue
        path = os.path.join(dirpath, filename)
        rel = os.path.relpath(path, root).replace("\\", "/")
        text = open(path, encoding="utf-8").read()

        if rel.startswith("partials/"):
          partial_def_files.append(rel)
          id_match = re.search(r'(?m)^[ \t]*id:\s*(.+?)\s*$', text)
          name_match = re.search(r'(?m)^[ \t]*name:\s*(.+?)\s*$', text)
          if not id_match or not name_match:
              print(f"Unable to resolve partial id/name from rendered partial file: {rel}")
              sys.exit(1)
          partial_defs.add((clean(id_match.group(1)), clean(name_match.group(1))))
          continue

        lines = text.splitlines()
        in_partials = False
        current_ref = None
        partial_indent = None

        for line_no, line in enumerate(lines, start=1):
            stripped = line.strip()
            indent = len(line) - len(line.lstrip())

            if stripped == "partials:":
                in_partials = True
                partial_indent = indent
                current_ref = None
                continue

            if not in_partials:
                continue

            if stripped and indent < partial_indent and stripped != "partials:":
                in_partials = False
                current_ref = None
                partial_indent = None
                continue

            id_match = re.match(r'^\s*-\s+id:\s*(.+?)\s*$', line)
            if id_match:
                current_ref = {"id": clean(id_match.group(1)), "line": line_no}
                continue

            if current_ref is None:
                continue

            name_match = re.match(r'^\s*name:\s*(.+?)\s*$', line)
            if name_match:
                current_ref["name"] = clean(name_match.group(1))
                continue

            path_match = re.match(r'^\s*path:\s*(.+?)\s*$', line)
            if path_match:
                current_ref["path"] = clean(path_match.group(1))
                partial_refs.append((current_ref["id"], current_ref.get("name", ""), rel, current_ref["line"]))
                current_ref = None

if not partial_def_files:
    print("No rendered partial definition files were found.")
    sys.exit(1)

missing = []
for ref_id, ref_name, rel, line_no in partial_refs:
    if (ref_id, ref_name) not in partial_defs:
        missing.append((ref_id, ref_name, rel, line_no))

