select_partial_template() {
  local ce_template="$1"
  local ee_template="$2"
  local output_file="$3"
  local partial_type="$4"

  case "$partial_type" in
    redis-ce)
      mv "$ce_template" "$output_file"
      rm -f "$ee_template"
      ;;
    redis-ee)
      mv "$ee_template" "$output_file"
      rm -f "$ce_template"
      ;;
    *)
      echo "Unsupported Redis partial type for $output_file: $partial_type"
      exit 1
      ;;
  esac
}

